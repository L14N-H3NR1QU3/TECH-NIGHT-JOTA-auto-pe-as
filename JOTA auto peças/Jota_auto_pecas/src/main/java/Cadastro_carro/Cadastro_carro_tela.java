            String usuario = "root";
            String senha = "";
            
            conexao = DriverManager.getConnection (url, usuario, senha) ;
            String sql = "INSERT INTO carros(placa_carro,modelo_carro,data_entrada_carro) VALUES (?, ?, ?)";
            
